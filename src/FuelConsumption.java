public class FuelConsumption {
    String name;
    String carName;

    //constructor
    public FuelConsumption(String name, String carName) {
        this.name = name;
        this.carName = carName;
    }
        //Return method
        public String returnName() {
            return name;
        }
        public String returnCarName() {
            return carName;
        }
    }

